    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        String mid = jTextField1.getText();
        String weight = jTextField2.getText();
        String height = jTextField7.getText();
        String age = jTextField4.getText();
        String ablity = "";
        if (r1.isSelected()) {
            ablity = "Disablity";
        }
        if (r2.isSelected()) {
            ablity = "No Disablity";
        }
        String dor = jDateChooser1.getDate().toString();
        String deficiency = getSetectedCheckboxes(jPanel6);
        String comments = jTextArea1.getText();
        boolean b = DBUtils.dataExists("select * from doctorreport where mid like '" + mid + "'");
        if (b) {
            String sql = "update doctorreport set dor=?, weight=?, height=?, deficiency=?, ablity=?, comments=?, docid=?,awcid=?,age=? where mid like '" + mid + "'";
            int i = DBUtils.executeUpdate(sql, dor, weight, height, deficiency, ablity, comments, currentLoginModel.getDid(), currentLoginModel.getAwcid(), age);
            if (i > 0) {
                JOptionPane.showMessageDialog(this, "Health Record Inserted Successfully..!!");
            } else {
                JOptionPane.showMessageDialog(this, "Error In DB..??");
            }
        } else {
            String sql = "insert into doctorreport (mid, dor, weight, height, deficiency, ablity, comments, docid,awcid,age) values(?,?,?,?,?,?,?,?,?,?)";
            int i = DBUtils.executeUpdate(sql, mid, dor, weight, height, deficiency, ablity, comments, currentLoginModel.getDid(), currentLoginModel.getAwcid(), age);
            if (i > 0) {
                JOptionPane.showMessageDialog(this, "Health Record Inserted Successfully..!!");
            } else {
                JOptionPane.showMessageDialog(this, "Error In DB..??");
            }
        }
    }//GEN-LAST:event_jButton5ActionPerformed
    
