    public String getSetectedCheckboxes(JPanel panel) {
        String values = "";
        for (Component component : panel.getComponents()) {
            if (component instanceof JCheckBox) {
                JCheckBox c = (JCheckBox) component;
                if (c.isSelected()) {
//            return true;
                    values += c.getText() + ",";
                }
            }
        }
        return values;
    }

