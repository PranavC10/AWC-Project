    void refreshTeacherTable() {
        model1.setRowCount(0);
        if (teacherList != null && teacherList.size() > 0) {
            for (int i = 0; i < teacherList.size(); i++) {
                TeacherModel ch = teacherList.get(i);
                model1.addRow(new Object[]{(i + 1), ch.getTid(), ch.getFname() + " " + ch.getMname() + " " + ch.getLname(), ch.getAddress(), ch.getAwcid()});
            }

        }
    }

    void refreshDoctorTable() {
        model2.setRowCount(0);
        if (doctorList != null && doctorList.size() > 0) {
            for (int i = 0; i < doctorList.size(); i++) {
                DoctorModel ch = doctorList.get(i);
                model2.addRow(new Object[]{(i + 1), ch.getDid(), ch.getFname() + " " + ch.getMname() + " " + ch.getLname(), ch.getAddress(), ch.getAwcid()});
            }

        }
    }
